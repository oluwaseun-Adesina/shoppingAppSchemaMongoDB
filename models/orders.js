const mongoose = require('mongoose');

// create order schema
const orderSchema = new mongoose.Schema({
    customerId: {
        type: String,
        required: true
    },
    products: [
        {
            productId: {
                type: String,
                required: true
            },
            quantity: {
                type: Number,
                required: true
            }
        }
    ],
    total: {
        type: Number,
        required: true
    },
    status: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        required: true
    },
    address: {
        type: String,
        required: true
    }
});

// export order model
module.exports = mongoose.model('Order', orderSchema);